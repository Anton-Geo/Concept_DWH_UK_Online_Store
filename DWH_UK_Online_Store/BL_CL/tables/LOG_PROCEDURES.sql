CREATE TABLE "BL_CL"."LOG_PROCEDURES" (
    "LOG_ID"             BIGINT       PRIMARY KEY,
    "PROCEDURE_NAME"     VARCHAR(255) NOT NULL,
    "TABLE_NAME"         VARCHAR(255) NOT NULL,
    "INSERT_ROW_CNT"     BIGINT       NOT NULL,
    "UPDATE_ROW_CNT"     BIGINT       NOT NULL,
