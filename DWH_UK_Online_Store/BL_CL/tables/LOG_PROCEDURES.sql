    "IS_SUCCESS"         VARCHAR(1)   NOT NULL,
    "EXECITED_TIME_SEC"  FLOAT        NOT NULL,
    "TA_INSERT_DT"       TIMESTAMP    NOT NULL,
    "TA_UPDATE_DT"       TIMESTAMP    NOT NULL
);